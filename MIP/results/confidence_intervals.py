import numpy as np
import scipy.stats as stats

def calculate_confidence_interval(data):
    # Step 1: Calculate the mean
    mean = np.mean(data)

    # Step 2: Calculate the standard deviation
    std_dev = np.std(data, ddof=1)  # Assuming sample data, change to ddof=0 for population data

    # Step 3: Determine the sample size or population size
    n = len(data)

    # Step 4: Determine the critical value
    critical_value = stats.t.ppf(0.975, df=n-1)  # For a 95% confidence interval with n-1 degrees of freedom

    # Step 5: Calculate the margin of error
    margin_of_error = critical_value * (std_dev / np.sqrt(n))

    # Step 6: Compute the lower and upper bounds
    lower_bound = mean - margin_of_error
    upper_bound = mean + margin_of_error

    # Step 7: Return the mean and confidence interval
    return mean, margin_of_error

